<h2>Apa Itu Framework?</h2>
<p> Framework adalah kerangka kerja yang digunakan untuk mengembangkan aplikasi berbasis desktop atau aplikasi berbasis website. Dengan menggunakan framework Anda akan lebih mudah untuk membuat aplikasi atau website. Itu karena Anda hanya perlu menyusun komponen-komponen pemrograman yang sudah jadi. Dengan kata lain, Anda tidak perlu membuat berbagai fitur dari awal lagi.

    Framework sengaja diciptakan untuk membantu developer mengembangkan aplikasi atau website lebih cepat serta tersusun dan terstruktur. 
Dengan begitu, developer dan programmer tidak perlu melakukan koding program yang diulang-ulang.

</p>
<h2>Fungsi Framework</h2>
<p> Framework memiliki fungsi utama untuk mempermudah para developer mengembangkan aplikasi dan website terkait struktur MVC (Model View Controller) yang digunakan. Selain itu, ada tiga fungsi framework yang dapat Anda ketahui di bawah ini:
</p>
<h3>1. Program Menjadi Lebih Terstruktur dan Tersusun</h3>
<p> Saat developer mengerjakan pengembangan aplikasi atau website yang besar, maka program yang akan ditulis di dalamnya menjadi semakin banyak. 
Terkadang, semakin banyaknya program yang ditulis akan menjadikan proses debugging semakin lambat. Selain itu, saat Anda mencari kode program yang error akan semakin sulit karena program yang tidak terstruktur.</p>

<h3>2. Praktis untuk Developer</h3>
<p> Saat Anda mengembangkan aplikasi atau website, akan lebih praktis ketika Anda menggunakan framework. 
Alasannya karena framework sudah menyediakan kode berupa function dan class jadi proses pembuatan software atau aplikasi akan menjadi lebih cepat. Anda cukup memanggil function atau class tersebut ke dalam kode program Anda.
</p>

<h3>3. Memiliki Keamanan yang Lebih Unggul</h3>
<p> Selama bertahun-tahun, keamanan untuk aplikasi dan website telah menjadi perhatian utama bagi developer. 
Beberapa dari developer telah menggunakan segala cara untuk  mengamankan aplikasi dan website yang telah mereka buat.
Setelah framework hadir, developer boleh sedikit tenang soal keamanan program yang telah dibuat. 
Karena setiap celah keamanan dari framework sudah diidentifikasi oleh 10-100+ developer ahli. 
Framework juga terus diperbarui versinya untuk menawarkan fitur baru dan menangani bug yang meminimalisir celah keamanan framework.</p>
